    private void onClickBtnConfirmar(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_onClickBtnConfirmar
        if (txtNome.getText().equals("")) {
            ShowMessage("Falta digitar o campo Nome");
            txtNome.requestFocus();
        } else if (txtLogin.getText().equals("")) {
            ShowMessage("Falta digitar o campo Login");
            txtLogin.requestFocus();
        } else if (pwdSenha.getPassword().length <= 0) {
            ShowMessage("Falta digitar o campo Senha");
            pwdSenha.requestFocus();
        } else if (pwdConfirmeASenha.getPassword().length <= 0) {
            ShowMessage("Falta digitar o campo Confirmar Senha");
            pwdConfirmeASenha.requestFocus();
        } else if (pwdSenha.getPassword().equals(pwdConfirmeASenha.getPassword())) {
            ShowMessage("As senhas nï¿½o conferem!");
            pwdConfirmeASenha.requestFocus();
        } else {
            ControllerUsuario controllerUsuario = new ControllerUsuario();
            try {
                controllerUsuario.cadastrar(txtNome.getText(), txtLogin.getText(), pwdSenha.getPassword());
            } catch (SQLException | NoSuchAlgorithmException | AgendaException e) {
                ShowMessage(e.getMessage());
            }
            onClickBtnCancelar(null);
        }
    }//GEN-LAST:event_onClickBtnConfirmar

